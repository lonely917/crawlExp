package yan.javatips.socket;

public class ElementKey {
	private int key_id;
	private int icon_id;
	private int rly_id;
	
	public int getKey_id() {
		return key_id;
	}
	public void setKey_id(int key_id) {
		this.key_id = key_id;
	}
	public int getIcon_id() {
		return icon_id;
	}
	public void setIcon_id(int icon_id) {
		this.icon_id = icon_id;
	}
	public int getRly_id() {
		return rly_id;
	}
	public void setRly_id(int rly_id) {
		this.rly_id = rly_id;
	}
}
